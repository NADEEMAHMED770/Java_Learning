public class Exercise01REVERSELOOP {
    public static void main(String[] args){
        // Scanner input = new Scanner(System.in);
        for (int i=1;i<=10;i++){
            System.out.println(i);
        }
        System.out.println();
        System.out.println("Reverse Loop");
        for (int i=10;i>=1;i--){
            System.out.println(i);
        }
        
    }
}
